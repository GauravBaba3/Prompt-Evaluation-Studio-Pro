"""Export service for PDF, CSV, Markdown, JSON, HTML."""

from __future__ import annotations

import csv
import json
from datetime import datetime, timezone
from io import BytesIO, StringIO
from pathlib import Path

from reportlab.lib import colors
from reportlab.lib.pagesizes import letter
from reportlab.lib.styles import getSampleStyleSheet
from reportlab.platypus import Paragraph, SimpleDocTemplate, Spacer, Table, TableStyle

from config.settings import get_settings
from utils.logger import get_logger

logger = get_logger(__name__)


class ExportService:
    def __init__(self) -> None:
        settings = get_settings()
        settings.ensure_directories()
        self.exports_dir = settings.exports_dir

    def _timestamp(self) -> str:
        return datetime.now(timezone.utc).strftime("%Y%m%d_%H%M%S")

    def export_json(self, data: dict | list, filename_prefix: str = "export") -> Path:
        path = self.exports_dir / f"{filename_prefix}_{self._timestamp()}.json"
        path.write_text(json.dumps(data, indent=2, default=str), encoding="utf-8")
        logger.info("Exported JSON to %s", path)
        return path

    def export_csv(self, rows: list[dict], filename_prefix: str = "export") -> Path:
        path = self.exports_dir / f"{filename_prefix}_{self._timestamp()}.csv"
        if not rows:
            path.write_text("", encoding="utf-8")
            return path
        output = StringIO()
        writer = csv.DictWriter(output, fieldnames=list(rows[0].keys()))
        writer.writeheader()
        writer.writerows(rows)
        path.write_text(output.getvalue(), encoding="utf-8")
        logger.info("Exported CSV to %s", path)
        return path

    def export_markdown(self, content: str, filename_prefix: str = "export") -> Path:
        path = self.exports_dir / f"{filename_prefix}_{self._timestamp()}.md"
        path.write_text(content, encoding="utf-8")
        logger.info("Exported Markdown to %s", path)
        return path

    def export_html(self, title: str, body_html: str, filename_prefix: str = "export") -> Path:
        html = f"""<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>{title}</title>
  <style>
    body {{ font-family: Arial, sans-serif; margin: 40px; line-height: 1.6; color: #1a1a2e; }}
    h1, h2, h3 {{ color: #16213e; }}
    table {{ border-collapse: collapse; width: 100%; margin: 16px 0; }}
    th, td {{ border: 1px solid #ddd; padding: 8px; text-align: left; }}
    th {{ background: #0f3460; color: white; }}
    .metric {{ background: #e94560; color: white; padding: 12px; border-radius: 8px; display: inline-block; margin: 8px; }}
  </style>
</head>
<body>
  <h1>{title}</h1>
  {body_html}
  <footer><p>Generated by Prompt Evaluation Studio Pro — {datetime.now(timezone.utc).isoformat()}</p></footer>
</body>
</html>"""
        path = self.exports_dir / f"{filename_prefix}_{self._timestamp()}.html"
        path.write_text(html, encoding="utf-8")
        logger.info("Exported HTML to %s", path)
        return path

    def export_pdf_report(self, title: str, sections: list[tuple[str, str]], filename_prefix: str = "report") -> Path:
        path = self.exports_dir / f"{filename_prefix}_{self._timestamp()}.pdf"
        buffer = BytesIO()
        doc = SimpleDocTemplate(str(path), pagesize=letter)
        styles = getSampleStyleSheet()
        story = [Paragraph(title, styles["Title"]), Spacer(1, 12)]

        for heading, content in sections:
            story.append(Paragraph(heading, styles["Heading2"]))
            story.append(Spacer(1, 6))
            for paragraph in content.split("\n"):
                if paragraph.strip():
                    story.append(Paragraph(paragraph.replace("<", "&lt;").replace(">", "&gt;"), styles["Normal"]))
            story.append(Spacer(1, 12))

        doc.build(story)
        logger.info("Exported PDF to %s", path)
        return path

    def export_comparison_pdf(self, results: list[dict], filename_prefix: str = "comparison") -> Path:
        path = self.exports_dir / f"{filename_prefix}_{self._timestamp()}.pdf"
        doc = SimpleDocTemplate(str(path), pagesize=letter)
        styles = getSampleStyleSheet()
        story = [Paragraph("Prompt Comparison Report", styles["Title"]), Spacer(1, 12)]

        table_data = [["Label", "Time (ms)", "Length", "Tokens", "Overall", "Winner"]]
        for row in results:
            table_data.append([
                str(row.get("label", "")),
                str(row.get("execution_time_ms", "")),
                str(row.get("output_length", "")),
                str(row.get("token_usage", "")),
                str(row.get("overall_score", "")),
                "Yes" if row.get("is_winner") else "No",
            ])

        table = Table(table_data, repeatRows=1)
        table.setStyle(
            TableStyle(
                [
                    ("BACKGROUND", (0, 0), (-1, 0), colors.HexColor("#0f3460")),
                    ("TEXTCOLOR", (0, 0), (-1, 0), colors.white),
                    ("GRID", (0, 0), (-1, -1), 0.5, colors.grey),
                    ("ROWBACKGROUNDS", (0, 1), (-1, -1), [colors.whitesmoke, colors.lightgrey]),
                ]
            )
        )
        story.append(table)
        doc.build(story)
        return path

    def build_experiment_markdown(self, experiments: list[dict]) -> str:
        lines = ["# Experiment History Export", ""]
        for exp in experiments:
            lines.extend(
                [
                    f"## {exp.get('title', 'Untitled')}",
                    f"- **Status:** {exp.get('status')}",
                    f"- **Model:** {exp.get('model_used')}",
                    f"- **Execution Time:** {exp.get('execution_time_ms')} ms",
                    f"- **Success:** {exp.get('success')}",
                    "",
                    "### Notes",
                    exp.get("notes") or "No notes",
                    "",
                    "### Response",
                    exp.get("response_text") or "No response",
                    "",
                    "---",
                    "",
                ]
            )
        return "\n".join(lines)
